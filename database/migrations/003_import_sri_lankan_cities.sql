SELECT name, latitude, longitude,
    ROUND(ST_Distance(
        geom,
        ST_SetSRID(ST_MakePoint(79.8612, 6.9271), 4326)::geography
    ) / 1000, 2) as distance_km
FROM cities
WHERE country = 'Sri Lanka'
    AND ST_DWithin(
        geom,
        ST_SetSRID(ST_MakePoint(79.8612, 6.9271), 4326)::geography,
        50000
    )
ORDER BY distance_km
LIMIT 10;
